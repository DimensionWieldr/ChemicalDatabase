    private void jButtonSubmitReactantsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSubmitReactantsActionPerformed
        this.jButtonTryAnother.setEnabled(true);
        this.jButtonSubmitReactants.setEnabled(false);
        this.jTextFieldReactant1.setEnabled(false);
        this.jTextFieldReactant2.setEnabled(false);
        
        Chemical tempChemical1 = this.databaseGUI.searchChemicalsByName(this.jTextFieldReactant1.getText());
        Chemical tempChemical2 = this.databaseGUI.searchChemicalsByName(this.jTextFieldReactant2.getText());
        
        if (tempChemical1 instanceof Element && tempChemical2 instanceof Element) {
            this.reactant1 = (Element)tempChemical1;
            this.reactant2 = (Element)tempChemical2;
            
            if (this.reactant1 == this.reactant2)
                this.sameReactants = true;
            
            //Reactant 1
            this.jLabelMassReactant1.setText("" + (this.reactant1.getNumProton() + this.reactant1.getNumNeutron()));
            this.jLabelProtonsReactant1.setText("" + this.reactant1.getNumProton());
            this.jLabelMolecularFormulaReactant1.setText(this.reactant1.getAtomicSymbol());
            this.jLabelOxNumReactant1.setText("" + this.reactant1.getOxidationNum());
            
            //Reactant 2
            this.jLabelMassReactant2.setText("" + (this.reactant2.getNumProton() + this.reactant1.getNumNeutron()));
            this.jLabelProtonsReactant2.setText("" + this.reactant2.getNumProton());
            this.jLabelMolecularFormulaReactant2.setText(this.reactant2.getAtomicSymbol());
            this.jLabelOxNumReactant2.setText("" + this.reactant2.getOxidationNum());
            
            this.reaction = this.databaseGUI.simulateReaction(this.reactant1, this.reactant2);
  
            if (this.reaction.getID() == Reaction.getREPEATED_REACTION_ID()) {
                JOptionPane.showMessageDialog(null, "Reaction already exists!");
            } else if (this.reaction.getID() == Reaction.getIMPOSSIBLE_REACTION_ID()) {
                JOptionPane.showMessageDialog(null, "Reaction not possible!");
            } else {
                JOptionPane.showMessageDialog(null, "New reaction successful!");
                
                this.jButtonSubmitProductProperties.setEnabled(true);
                this.setProductTextFieldsEnabled(true);
                this.noProduct = false;

                this.product = this.reaction.getProduct();
                
                //Check if Reactants are the same
                if (!this.sameReactants) {
                    //Product Formulas
                    this.jLabelProductA.setText(this.product.getComponent1().getComponent().getAtomicSymbol());
                    this.jLabelProductB.setText(this.product.getComponent2().getComponent().getAtomicSymbol());
                    this.jLabelEquationProductA.setText(this.product.getComponent1().getComponent().getAtomicSymbol());
                    this.jLabelEquationProductB.setText(this.product.getComponent2().getComponent().getAtomicSymbol());

                    //Subscripts
                    if (this.product.getComponent1().getNumOccurrence() > 1) {
                        this.jLabelProductSubscriptA.setText("" + this.product.getComponent1().getNumOccurrence());
                        this.jLabelEquationProductSubscriptA.setText("" + this.product.getComponent1().getNumOccurrence());
                    } else {
                        this.jLabelProductSubscriptA.setText("");
                        this.jLabelEquationProductSubscriptA.setText("");
                    }
                    if (this.product.getComponent2().getNumOccurrence() > 1) {
                        this.jLabelProductSubscriptB.setText("" + this.product.getComponent2().getNumOccurrence());
                        this.jLabelEquationProductSubscriptB.setText("" + this.product.getComponent2().getNumOccurrence());
                    } else {
                        this.jLabelProductSubscriptB.setText("");
                        this.jLabelEquationProductSubscriptB.setText("");
                    }
                } else {
                    this.jLabelProductA.setText("");
                    this.jLabelProductSubscriptA.setText("");
                    this.jLabelEquationProductA.setText("");
                    this.jLabelEquationProductSubscriptA.setText("");
                    
                    this.jLabelProductB.setText(this.product.getComponent2().getComponent().getAtomicSymbol());
                    this.jLabelProductSubscriptB.setText("" + (this.product.getComponent1().getNumOccurrence() + this.product.getComponent2().getNumOccurrence()));
                    this.jLabelEquationProductB.setText(this.product.getComponent2().getComponent().getAtomicSymbol());
                    this.jLabelEquationProductSubscriptB.setText("" + (this.product.getComponent1().getNumOccurrence() + this.product.getComponent2().getNumOccurrence()));
                }
                
                //Reactants in Equation
                this.jLabelEquationReactant1.setText(this.reactant1.getAtomicSymbol());
                this.jLabelEquationReactant2.setText(this.reactant2.getAtomicSymbol());
                
                //Product Properties
                this.jLabelProductMolarMass.setText("Molar Mass: " + this.product.getMolarMass());
                
                if (this.product.getStpState() == Chemical.DEFAULT_STATE)
                    this.jLabelProductStpState.setText("State @ STP: " + Chemical.DEFAULT_COMMON_NAME);
                else
                    this.jLabelProductStpState.setText("State @ STP: " + this.product.getStpState());
                
                //Set Charges for both
                int charge = this.product.getCharge();
                if (charge != 0) {
                    if (charge > 0) {
                        this.jLabelProductCharge.setText("+" + charge);
                        this.jLabelEquationProductCharge.setText("+" + charge);
                    } else {
                        this.jLabelProductCharge.setText("" + charge);
                        this.jLabelEquationProductCharge.setText("" + charge);
                    }
                } else {
                    this.jLabelProductCharge.setText("");
                    this.jLabelEquationProductCharge.setText("");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Reactant not found!");
        }
    }//GEN-LAST:event_jButtonSubmitReactantsActionPerformed

