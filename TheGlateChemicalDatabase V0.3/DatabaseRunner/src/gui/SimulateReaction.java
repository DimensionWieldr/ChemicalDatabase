    private void jButtonSubmitReactantsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSubmitReactantsActionPerformed
        this.jButtonTryAnother.setEnabled(true);
        this.jButtonSubmitReactants.setEnabled(false);
        this.jTextFieldReactant1.setEnabled(false);
        this.jTextFieldReactant2.setEnabled(false);
        
        Chemical tempChemical1 = DatabaseGUI.getDatabase().searchChemicalsByName(this.jTextFieldReactant1.getText());
        Chemical tempChemical2 = DatabaseGUI.getDatabase().searchChemicalsByName(this.jTextFieldReactant2.getText());
        
        if (tempChemical1 instanceof Element && tempChemical2 instanceof Element) {
            reactant1 = (Element)tempChemical1;
            reactant2 = (Element)tempChemical2;
            
            if (reactant1 == reactant2) sameReactants = true;
            
            //Reactant 1
            this.jLabelMassReactant1.setText("" + (reactant1.getNumProton() + reactant1.getNumNeutron()));
            this.jLabelProtonsReactant1.setText("" + reactant1.getNumProton());
            this.jLabelMolecularFormulaReactant1.setText(reactant1.getMolecularFormula());
            this.jLabelOxNumReactant1.setText("" + reactant1.getOxidationNum());
            
            //Reactant 2
            this.jLabelMassReactant2.setText("" + (reactant2.getNumProton() + reactant1.getNumNeutron()));
            this.jLabelProtonsReactant2.setText("" + reactant2.getNumProton());
            this.jLabelMolecularFormulaReactant2.setText(reactant2.getMolecularFormula());
            this.jLabelOxNumReactant2.setText("" + reactant2.getOxidationNum());
            
            id = DatabaseGUI.getDatabase().getReactionManager().getReactionList().size();
            
            reaction = DatabaseGUI.simulateReaction(id, reactant1, reactant2);
  
            if (reaction != null) {
                JOptionPane.showMessageDialog(null, "New reaction successful!");
                
                this.jButtonSubmitProductName.setEnabled(true);
                this.jTextFieldProductName.setEnabled(true);
                noProduct = false;

                product = reaction.getProduct();
                
                //Check if Reactants are the same
                if (!sameReactants) {
                    //Product Formulas
                    this.jLabelProductA.setText(product.getComponent1().getComponent().getMolecularFormula());
                    this.jLabelProductB.setText(product.getComponent2().getComponent().getMolecularFormula());
                    this.jLabelEquationProductA.setText(product.getComponent1().getComponent().getMolecularFormula());
                    this.jLabelEquationProductB.setText(product.getComponent2().getComponent().getMolecularFormula());

                    //Subscripts
                    if (product.getComponent1().getNumOccurrence() > 1) {
                        this.jLabelProductSubscriptA.setText("" + product.getComponent1().getNumOccurrence());
                        this.jLabelEquationProductSubscriptA.setText("" + product.getComponent1().getNumOccurrence());
                    } else {
                        this.jLabelProductSubscriptA.setText("");
                        this.jLabelEquationProductSubscriptA.setText("");
                    }
                    if (product.getComponent2().getNumOccurrence() > 1) {
                        this.jLabelProductSubscriptB.setText("" + product.getComponent2().getNumOccurrence());
                        this.jLabelEquationProductSubscriptB.setText("" + product.getComponent2().getNumOccurrence());
                    } else {
                        this.jLabelProductSubscriptB.setText("");
                        this.jLabelEquationProductSubscriptB.setText("");
                    }
                } else {
                    this.jLabelProductA.setText("");
                    this.jLabelProductSubscriptA.setText("");
                    this.jLabelEquationProductA.setText("");
                    this.jLabelEquationProductSubscriptA.setText("");
                    
                    this.jLabelProductB.setText(product.getComponent2().getComponent().getMolecularFormula());
                    this.jLabelProductSubscriptB.setText("" + (product.getComponent1().getNumOccurrence() + product.getComponent2().getNumOccurrence()));
                    this.jLabelEquationProductB.setText(product.getComponent2().getComponent().getMolecularFormula());
                    this.jLabelEquationProductSubscriptB.setText("" + (product.getComponent1().getNumOccurrence() + product.getComponent2().getNumOccurrence()));
                }
                
                //Reactants in Equation
                this.jLabelEquationReactant1.setText(reactant1.getMolecularFormula());
                this.jLabelEquationReactant2.setText(reactant2.getMolecularFormula());
                
                //Product Properties
                this.jLabelProductMolarMass.setText("Molar Mass: " + product.getMolarMass());
                this.jLabelProductStpDensity.setText("Density @ STP: " + product.getStpDensity());
                this.jLabelProductStpState.setText("State @ STP: " + product.getStpState());
                this.jLabelProductMP.setText("Melting Point: " + product.getMP());
                this.jLabelProductBP.setText("Boiling Point: " + product.getBP());
                this.jLabelProductColour.setText("Colour: " + product.getColour());
                
                //Set Charges for both
                int charge = product.getCharge();
                if (charge != 0) {
                    if (charge > 0) {
                        this.jLabelProductCharge.setText("+" + charge);
                        this.jLabelEquationProductCharge.setText("+" + charge);
                    } else {
                        this.jLabelProductCharge.setText("" + charge);
                        this.jLabelEquationProductCharge.setText("" + charge);
                    }
                } else {
                    this.jLabelProductCharge.setText("");
                    this.jLabelEquationProductCharge.setText("");
                }
            } else {
                if (reactant1.getOxidationNum() == 0 || reactant2.getOxidationNum() == 0) 
                    JOptionPane.showMessageDialog(null, "Reaction not possible!");
                else 
                    JOptionPane.showMessageDialog(null, "Reaction already exists!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Reactant not found!");
        }
    }//GEN-LAST:event_jButtonSubmitReactantsActionPerformed

